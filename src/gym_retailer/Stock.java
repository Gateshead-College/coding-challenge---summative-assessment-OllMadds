package gym_retailer;

public class Stock {
    String name;
    double price;
    String manufacturer;
    int id;
    int numInStock;

    public Stock(String name, double price, String manufacturer, int id, int numInStock){
        this.name = name;
        this.price = price;
        this.manufacturer = manufacturer;
        this.id = id;
        this.numInStock = numInStock;
    }
    }

